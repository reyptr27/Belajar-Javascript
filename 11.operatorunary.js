let result = 1; //result = 1

document.writeln(result);

//paragraf break
document.writeln('<br>');

//mengubah menjadi bilangan positif
result = +result;

document.writeln(result);

//paragraf break
document.writeln('<br>');

//mengubah menjadi bilangan negatif
result = -result;

document.writeln(result);

//paragraf break
document.writeln('<br>');

//result =  + 1 (increment)
result++;

document.writeln(result);

//paragraf break
document.writeln('<br>');

//result =  - 1 (decrement)
result--;//5pangkat5 = 3125

document.writeln(result);
