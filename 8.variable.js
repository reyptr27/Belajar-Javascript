//menggunakan variable var
var firstName;

firstName = "Hello World!";

document.writeln(firstName);

//paragraf break
document.writeln('<br>');

//menggunakan variable tanpa deklarasi terlebih dahulu
var middleName = "Hello World!";

document.writeln(middleName);

//paragraf break
document.writeln('<br>');

//variable let dan const
//diwajibkan menggunakan variable let atau cons
//karena menggunakan var sudah lama dan tidak disarankan

//variable let (sama konsepnya dengan variable var)
let lastName = 'Hello World!';

document.writeln(lastName);

//paragraf break
document.writeln('<br>');

//variable const
//variable ini nilainya tidak dapat diubah - ubah atau bernilai konstan
//apabila mencoba dirubah akan mengalami error
const fullName = 'Hello World!';

document.writeln(fullName);
